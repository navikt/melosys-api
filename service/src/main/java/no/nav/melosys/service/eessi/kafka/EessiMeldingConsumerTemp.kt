private val log = KotlinLogging.logger {}

@Service
@Profile("!local-q1 & !local-q2")
class EessiMeldingConsumerTemp(
    private val prosessinstansService: ProsessinstansService,
    @Value("\${KAFKA_AIVEN_EESSIMELDING_DRYRUN:true}") private val dryrun: Boolean
) {

    init {
        log.info("Starter EessiMeldingConsumerTemp med dryrun=$dryrun, offset filtering liste=${listOfOffsets.size}")
    }

    private var errorSleepTime = 100L

    @KafkaListener(
        clientIdPrefix = "aiven-melosys-eessi-consumer",
        topics = ["\${kafka.aiven.eessi.topic}"],
        containerFactory = "aivenEessiMeldingListenerContainerFactoryTemp"
    )
    fun mottaMeldingAiven(
        consumerRecord: ConsumerRecord<String?, MelosysEessiMelding?>, @Headers header: Map<String, ByteArray>
    ) {
        putToMDC(MDCOperations.CORRELATION_ID, getCorrelationId(header))
        val melding = consumerRecord.value()
        val offset: Long = consumerRecord.offset()

        if (offset !in listOfOffsets) {
            return
        }

        log.info("Rekjører melding fra eessi(aiven): $offset")

        try {
            if (dryrun) {
                log.info("Dryrun - Mottatt melding fra eessi(aiven): $melding")
                return
            }
            prosessinstansService.opprettProsessinstansSedMottak(melding)
            Thread.sleep(errorSleepTime)
        } catch (e: Exception) {
            log.error("Rekjøreing - Feil ved mottak av SED(aiven)! ConsumerRecord.key: {}", consumerRecord.key(), e)
            errorSleepTime += 5000
        } finally {
            MDC.remove(MDCOperations.CORRELATION_ID)
        }
    }

    private fun getCorrelationId(header: Map<String, ByteArray>): String {
        val bytes = header[MDCOperations.CORRELATION_ID]
        return if (bytes != null && bytes.size > 0) {
            String(bytes)
        } else {
            UUID.randomUUID().toString()
        }
    }

